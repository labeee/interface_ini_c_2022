function classificar(){

  var tipo = document.getElementById('tipo_predominante').value
  var ZB = document.getElementById('numero_zb').innerHTML
  var fator_forma = String(parseFloat(document.getElementById('fator_de_forma').value))

  //classificação da envoltória
  CRDA = array_classe.filter(item => item.tipologia == tipo && item.ZB == ZB && item.FF_inf < fator_forma && item.FF_sup >= fator_forma)[0].crcgtt_env // CRDA é o coeficiente de redução de D para A
  var rowCount = $('#myTable tr').length;
  var rowCount = rowCount - 1 // a primeira linha é o cabeçalho da tabela
  var cargas_real = 0
  var cargas_ref = 0

  array_cargas_ref = [] //array para verificar se existem cargas não calculadas
  var consumo_equipamentos = 0 //consumo relativo aos equipamentos da zona térmica

  //começa conferindo se foram realizados os cálculos e somando as cargas calculadas
  
  if (document.getElementById('check_simulacao').checked){ //se for pelo método de simulação, apenas toma os dados inseridos
    var cargas_real = document.getElementById('carga_sim_real').value
    var cargas_ref = document.getElementById('carga_sim_ref').value
    var consumo_equipamentos = document.getElementById('equipamentos_simulacao').value
    if (consumo_equipamentos == NaN){
      consumo_equipamentos = 0
    }
  }

  else{ //para quando for pelo método simplificado, a dpe é inserida, então calcula-se automaticamente o consumo de equipamentos
    for (var i = 0; i < rowCount; i++) {
      cargas_real += parseFloat(document.getElementById('cgtt_zona'+i).value)
      cargas_ref += parseFloat(document.getElementById('cgtt_ref'+i).value)
      array_cargas_ref.push((parseFloat(document.getElementById('cgtt_ref'+i).value)))
      dados_zt = uso_zonas.filter(uso => uso.nome == document.getElementById('lista_usos'+i).value)[0] //lista para tomar as informações de dpe e horas de uso
      consumo_equipamentos+=(parseFloat(document.getElementById('dpe_zona'+i).value)*parseFloat(dados_zt.horas_ocupacao)*parseFloat(document.getElementById('area_zona'+i).value))
    }
  }
  
  classificacao_envoltoria = 'Zero' // deixar o padrão como zero
  
  REDCEP =  ((cargas_ref - cargas_real)/(cargas_ref))*100
  if (cargas_ref == cargas_real){REDCEP = 0}
  i = (CRDA*100)/3

  
  if (REDCEP > (3*i)){classificacao_envoltoria = 'A'}
  else if ((2*i) <= REDCEP && REDCEP <= (3*i)){classificacao_envoltoria = 'B'}
  else if ((i) < REDCEP  && REDCEP < (2*i)){classificacao_envoltoria = 'C'}
  else if (0 <= REDCEP  && REDCEP <= (i)){classificacao_envoltoria = 'D'}
  else if (REDCEP < 0){classificacao_envoltoria = 'E'}
  
  //classificação iluminação
  ilum_refD = parseFloat(document.getElementById('iluminacao_referencia').value)
  ilum_real = parseFloat(document.getElementById('iluminacao_real').value)
  j = parseFloat(document.getElementById('iluminacao_i').value)
  
  dif_iluminacao = (ilum_refD-ilum_real)/ilum_refD*100
  if (ilum_refD == ilum_real){dif_iluminacao = 0}

  classificacao_ilum = 'Zero' //zero como padrão em caso de erros

  if (dif_iluminacao > (3*j)){classificacao_ilum = 'A'}
  else if ((2*j) <= dif_iluminacao && dif_iluminacao <= (3*j)){classificacao_ilum = 'B'}
  else if ((j) < dif_iluminacao  && dif_iluminacao <= (2*j)){classificacao_ilum = 'C'}
  else if (0 <= dif_iluminacao  && dif_iluminacao <= j){classificacao_ilum = 'D'}
  else if (dif_iluminacao < 0){classificacao_ilum = 'E'}


  //classificação da água quente
  sistema_aq = document.getElementById('sistema_aq').value

  //garantir que não preenchidos sejam 0
  aq_real_eletrica = parseFloat(document.getElementById('aq_real_eletrica').value)
  aq_real_termica = parseFloat(document.getElementById('aq_real_termica').value)
  aq_referencia = parseFloat(document.getElementById('aq_referencia').value)
  aq_real = parseFloat(aq_real_eletrica)*1.6+parseFloat(aq_real_termica)*1.1
  aq_ref = parseFloat(aq_referencia)*1.6
  
  dif_sistema_aq = ((aq_ref-aq_real)/aq_ref)*100
  if (aq_real == aq_ref){dif_sistema_aq = 0}

  if(sistema_aq == 'Com acumulação'){
      if (dif_sistema_aq > 30){classificacao_sistema_aq = "A"}
      else if (20 < dif_sistema_aq && dif_sistema_aq <= 30  ){classificacao_sistema_aq = "B"}
      else if (10 < dif_sistema_aq && dif_sistema_aq <= 20){classificacao_sistema_aq = "C"}
      else if (0 < dif_sistema_aq && dif_sistema_aq <= 10){classificacao_sistema_aq = "D"}
      else if (dif_sistema_aq < 0){classificacao_sistema_aq = "E"}
  }


  else if(sistema_aq == 'Sem acumulação'){
    if (dif_sistema_aq > 21){classificacao_sistema_aq = "A"}
    else if (14 < dif_sistema_aq && dif_sistema_aq <=21 ){classificacao_sistema_aq = "B"}
    else if (7 < dif_sistema_aq && dif_sistema_aq <= 14){classificacao_sistema_aq = "C"}
    else if (0 < dif_sistema_aq && dif_sistema_aq <= 7){classificacao_sistema_aq = "D"}
    else if (dif_sistema_aq < 0){classificacao_sistema_aq = "E"}
  }

  
  // AR CONDICIONADO

  //determinação de qual carga térmica será tomada, conforme o método de condicionamento (quando ventilado pode ser zero, e quando híbrido tem redução no consumo)
  var mensagem_ventilado = ''

  //se totalmente condicionada
  if (document.getElementById('check_condicionada').checked){
    var consumo_ac_real = parseFloat(document.getElementById('consumo_ac_real_cond').value)
    var cond_a = document.getElementById('cond_a_ac').value;
  }

  //se em sistema híbrido
  if (document.getElementById('check_hibrida').checked){
    var PHOCT = document.getElementById('PHOCT_hibrido').value;
    var cond_a = document.getElementById('cond_a_hibrido').value;

    if (PHOCT < 90){
      var consumo_ac_real =  document.getElementById('consumo_ac_real_hibrido').value*((100-PHOCT)/100)
    }
    else{
      var consumo_ac_real = 0
    }
    
  }

  if (document.getElementById('check_ventilada').checked){
    var PHOCT = document.getElementById('PHOCT_ventilado').value;
    if (PHOCT < 90){
      mensagem_ventilado = 'Atenção, caso sua edificação seja apenas naturalmente ventilada, o PHOCT deve ser igual ou superior a 90%; caso contrário é necessário prever um sistema de condicionamento artificia!'
    }
    else{
      consumo_ac_real = 0
    }
  }

  classificacao_sistema_ac = 'Zero' //manter como padrão

  //classificação sistema AC
  var consumo_ac_ref = cargas_ref / 2.6
  var dif_sistema_ac = (((consumo_ac_ref - consumo_ac_real)/(consumo_ac_ref))*100)
  if (consumo_ac_real == consumo_ac_ref){dif_sistema_ac = 0}
  if (PHOCT != undefined && PHOCT > 90 && consumo_ac_real == 0){dif_sistema_ac = 100}

  if (['01','02','03'].includes(ZB)){
    if (dif_sistema_ac > 51){classificacao_sistema_ac = "A"}
    else if (34 < dif_sistema_ac && dif_sistema_ac <=51 ){classificacao_sistema_ac = "B"}
    else if (17 < dif_sistema_ac && dif_sistema_ac <= 34){classificacao_sistema_ac = "C"}
    else if (0 < dif_sistema_ac && dif_sistema_ac <= 17){classificacao_sistema_ac = "D"}
    else if (dif_sistema_ac < 0){classificacao_sistema_ac = "E"}
  }
  else if (['04','05','06'].includes(ZB)){
    if (dif_sistema_ac > 48){classificacao_sistema_ac = "A"}
    else if (32 < dif_sistema_ac && dif_sistema_ac <= 48 ){classificacao_sistema_ac = "B"}
    else if (16 < dif_sistema_ac && dif_sistema_ac <= 32){classificacao_sistema_ac = "C"}
    else if (0 < dif_sistema_ac && dif_sistema_ac <= 16){classificacao_sistema_ac = "D"}
    else if (dif_sistema_ac < 0){classificacao_sistema_ac = "E"}
  }
  else if (['07','08'].includes(ZB)){
    if (dif_sistema_ac > 43){classificacao_sistema_ac = "A"}
    else if (29 < dif_sistema_ac && dif_sistema_ac <= 43 ){classificacao_sistema_ac = "B"}
    else if (14 < dif_sistema_ac && dif_sistema_ac <= 29){classificacao_sistema_ac = "C"}
    else if (0 < dif_sistema_ac && dif_sistema_ac <= 14){classificacao_sistema_ac = "D"}
    else if (dif_sistema_ac < 0){classificacao_sistema_ac = "E"}
  }


  //classificação geral
  var geracao = parseFloat(document.getElementById('geracao').value)
  if (geracao == NaN || geracao == ''){geracao = 0}
  if (isNaN(aq_real) || isNaN(aq_ref)){aq_real_conta = 0} else{aq_real_conta = aq_real}
  if (isNaN(aq_ref) || isNaN(aq_real)){aq_ref_conta = 0} else {aq_ref_conta = aq_ref} // se um dos dois não for preenchido, o outro não pode entrar na conta do consumo total
  var CEPreal = parseFloat(consumo_ac_real)*1.6 + parseFloat(aq_real_conta) + parseFloat(consumo_equipamentos*1.6) - parseFloat(geracao)*1.6
  var CEPref =  parseFloat(consumo_ac_ref)*1.6 + parseFloat(aq_ref_conta) + parseFloat(consumo_equipamentos*1.6)
  
  //definir o tipo de sistema de apoio para selecionar o coeficiente de emissões de CO2 da parte térmica
  var tipo_apoio = document.getElementById('apoio_aq').value
  if(tipo_apoio == 'GLP'){
    var emissao_termica = 0.227
  }
  else if (tipo_apoio == 'GNV'){
    var emissao_termica = 0.202
  }
  else{var emissao_termica = 0}

  //definir se é parte de  Sistema Interligado Nacional (SIN) ou não para definição das emissões de CO2 por eletricidade
  if (document.getElementById('sistema_eletricidade').value == 'Sistema Interligado Nacional'){
    var emissao_eletricidade = 0.09
  }
  else{var emissao_eletricidade = 0.753}

  if (isNaN(aq_real_eletrica)|| isNaN(aq_real_termica) || isNaN(aq_ref)){aq_real_eletrica_conta = 0} else{aq_real_eletrica_conta = aq_real_eletrica}
  if (isNaN(aq_real_termica) || isNaN(aq_real_eletrica) || isNaN(aq_ref)){aq_real_termica_conta = 0} else{aq_real_termica_conta = aq_real_termica}

  var emissao_real = parseFloat(consumo_ac_real)*1.6*emissao_eletricidade + parseFloat(aq_real_eletrica_conta)*1.6*emissao_eletricidade+ parseFloat(aq_real_termica_conta)*1.1*emissao_termica + parseFloat(consumo_equipamentos)*1.6*emissao_eletricidade - parseFloat(geracao)*1.6*emissao_eletricidade
  var emissao_ref =  parseFloat(consumo_ac_ref)*1.6*emissao_eletricidade + parseFloat(aq_ref_conta)*emissao_eletricidade + parseFloat(consumo_equipamentos)*1.6*emissao_eletricidade

  red_emissoes = (emissao_ref - emissao_real)/(emissao_ref)*100
  REDCEP_geral = (CEPref - CEPreal)/CEPref*100
  if (CEPref == CEPreal){REDCEP_geral = 0}
  
  CRCEPDA = array_total.filter(item => item.tipologia == tipo && item.ZB == ZB && item.FF_inf < fator_forma && item.FF_sup > fator_forma)[0].crcgtt_geral
  i = (CRCEPDA*100)/3
  
  classificacao_ed = 'Zero'

  if (REDCEP_geral > (3*i)){classificacao_ed = 'A'}
  else if ((2*i) <= REDCEP_geral && REDCEP_geral <= (3*i)){classificacao_ed = 'B'}
  else if ((i) < REDCEP_geral  && REDCEP_geral < (2*i)){classificacao_ed = 'C'}
  else if (0 <= REDCEP_geral  && REDCEP_geral <= (i)){classificacao_ed = 'D'}
  else if (REDCEP_geral < 0){classificacao_ed = 'E'}
  else{classificacao_ed = 'erro'}

  //classificação geral sem geracao

  var CEPreal_sem = parseFloat(consumo_ac_real)*1.6 + parseFloat(aq_real_conta) + parseFloat(consumo_equipamentos*1.6)
  var CEPref_sem =  parseFloat(consumo_ac_ref)*1.6 + parseFloat(aq_ref_conta) + parseFloat(consumo_equipamentos*1.6)
  REDCEP_geral_sem = (CEPref_sem - CEPreal_sem)/CEPref_sem*100
  if (CEPref_sem == CEPreal_sem){REDCEP_geral_sem = 0}
  
  classificacao_ed_sem = 'Zero'

  if (REDCEP_geral_sem > (3*i)){classificacao_ed_sem = 'A'}
  else if ((2*i) <= REDCEP_geral_sem && REDCEP_geral_sem <= (3*i)){classificacao_ed_sem = 'B'}
  else if ((i) < REDCEP_geral_sem  && REDCEP_geral_sem < (2*i)){classificacao_ed_sem = 'C'}
  else if (0 <= REDCEP_geral_sem  && REDCEP_geral_sem <= (i)){classificacao_ed_sem = 'D'}
  else if (REDCEP_geral_sem < 0){classificacao_ed_sem = 'E'}
  else{classificacao_ed_sem = 'erro'}
  

  //após calculadas todas as classificações, devem ser buscados os erros e mensagens de alerta, colocar na tela e trocar as imagens de classificação

  //se não cumprir os requisitos do AC cai para B
  if(classificacao_sistema_ac == "A" && cond_a == 'Não'){
    classificacao_sistema_ac = "B"
  }

  if([consumo_ac_ref].includes(NaN) && (document.getElementById('check_ventilada').checked == false)){
    mensagem_ac = 'Existem erros na contabilização do consumo de ar condicionado, verifique o preenchimento das cargas térmicas ou dos CEE dos equipamentos de condicionamento de ar'
    classificacao_sistema_ac = 'erro'
  }

  // se a edificacao for totalmente ventilada deve-se checar o PHOCT
  if (document.getElementById('check_ventilada').checked){
    if (PHOCT < 90){
      mensagem_ventilado = 'Atenção, caso sua edificação seja apenas naturalmente ventilada, o PHOCT deve ser igual ou superior a 90%; caso contrário é necessário prever um sistema de condicionamento artificial'
      classificacao_envoltoria = 'Zero' //o nome base das classificações é class+
      classificacao_sistema_ac = 'Zero'
      dif_sistema_ac = '-'
    }
    else{
      var cond_a = 'Sim' // tem que colocar, mesmo sendo condição do AC, para que não de erro
      //zera o consumo e cargas e fica A na envoltória
      var consumo_ac_real = 0
      classificacao_envoltoria = 'A'
      cargas_real = 0
      REDCEP = 100
      //não avalia sistema de AC
      classificacao_sistema_ac = 'Zero'
      dif_sistema_ac = 0
    }
  }

  mensagem_cargas = ''
  if (document.getElementById('check_simulacao').checked && (document.getElementById('check_ventilada').checked == false) && ([cargas_ref, cargas_real].includes(0) || [cargas_ref, cargas_real].includes(NaN))){
    mensagem_cargas = 'O método de simulação foi marcado, mas existem cargas térmicas não preenchidas, verifique se você escolheu o método correto e fez o preenchimento da aba ENVOLTÓRIA'
    classificacao_envoltoria = 'Zero'
  }

  else if (array_cargas_ref.includes(NaN) && document.getElementById('check_simplificado').checked && (document.getElementById('check_ventilada').checked == false)){
    mensagem_cargas = 'Existem zonas térmicas com cargas não calculadas, verifique se você pressionou o botão "CALCULAR CARGAS TÉRMICAS"'
    classificacao_envoltoria = 'Zero'
  }

  if (mensagem_ventilado!= ''){
    classificacao_ed = 'Zero'
    classificacao_ed_sem = 'Zero'
    window.alert(mensagem_ventilado)
    document.getElementById('carga_envoltoria').innerHTML = '- kWh/ano' 
    document.getElementById('img_envoltoria').src = 'img/classZero.svg'
    document.getElementById('reducao_envoltoria').innerHTML = 'Redução em relação à condição de referência: - %'
  }

  else if (mensagem_cargas != ''){
    classificacao_ed = 'Zero'
    classificacao_ed_sem = 'Zero'
    window.alert(mensagem_cargas)
    document.getElementById('carga_envoltoria').innerHTML = '- kWh/ano' 
    document.getElementById('img_envoltoria').src = 'img/classZero.svg'
    document.getElementById('reducao_envoltoria').innerHTML = 'Redução em relação à condição de referência: - %'
  }

  else {
    document.getElementById('carga_envoltoria').innerHTML = cargas_real + ' kWh/ano' 
    document.getElementById('img_envoltoria').src = 'img/class'+classificacao_envoltoria+'.svg'
    document.getElementById('reducao_envoltoria').innerHTML = 'Redução em relação à condição de referência: '+ Math.round(REDCEP)+' %'
  }
  
  // ar condicionado


 if((consumo_ac_real == NaN || consumo_ac_real == 0) && (document.getElementById('check_condicionada').checked || document.getElementById('check_hibrida').checked)){
    window.alert('Verifique se foram calculadas e preenchidas todas as cargas térmicas e consumos de condicionamento!')
    classificacao_ed = 'erro'
    classificacao_ed_sem = 'erro'
    document.getElementById('consumo_ac').innerHTML = '- kWh/ano' 
    document.getElementById('img_ac').src = 'img/classZero.svg'
    document.getElementById('reducao_ac').innerHTML = 'Redução em relação à condição de referência: - %'
  }
  else if (classificacao_sistema_ac == 'erro'){
    window.alert(mensagem_ac)
    classificacao_ed = 'erro'
    classificacao_ed_sem = 'erro'
    document.getElementById('consumo_ac').innerHTML = '- kWh/ano' 
    document.getElementById('img_ac').src = 'img/classZero.svg'
    document.getElementById('reducao_ac').innerHTML = 'Redução em relação à condição de referência: - %'
  }
  
  else{
    document.getElementById('consumo_ac').innerHTML = Math.round(consumo_ac_real*1.6) + ' kWh/ano' 
    document.getElementById('img_ac').src = 'img/class'+classificacao_sistema_ac+'.svg'
    document.getElementById('reducao_ac').innerHTML = 'Redução em relação à condição de referência: '+ Math.round(dif_sistema_ac)+' %'
  }

  

  //iluminação

  var mensagem_ilum = ''

  if ([ilum_refD, ilum_real, j].includes(NaN) || [ilum_refD, ilum_real, j].includes(0)){
    classificacao_ilum = 'Zero'
    mensagem_ilum = 'Verifique o preenchimento das informações relativas à iluminação, o não preenchimento resultará na não classificação do sistema'
  }
  
  if (mensagem_ilum != ''){
    window.alert(mensagem_ilum)
    document.getElementById('consumo_iluminacao').innerHTML = ' - kWh/ano' 
    document.getElementById('img_iluminacao').src = 'img/class'+classificacao_ilum+'.svg'
    document.getElementById('reducao_iluminacao').innerHTML = 'Redução em relação à condição de referência: - %'
  }

  else{
    document.getElementById('consumo_iluminacao').innerHTML = ilum_real*1.6 + ' kWh/ano' 
    document.getElementById('img_iluminacao').src = 'img/class'+classificacao_ilum+'.svg'
    document.getElementById('reducao_iluminacao').innerHTML = 'Redução em relação à condição de referência: '+ Math.round(dif_iluminacao)+' %'
  }

  
  //aquecimento de água
  mensagem_aq = ''

  if ([aq_real, aq_ref].includes(NaN)){
    classificacao_sistema_aq = 'Zero'
    mensagem_aq = 'Verifique o preenchimento das informações relativas ao aquecimento de água,  o não preenchimento resultará na não classificação do sistema'
  }

  if (mensagem_aq  != ''){
    window.alert(mensagem_aq)
    document.getElementById('consumo_aq').innerHTML = '- kWh/ano' 
    document.getElementById('img_aq').src = 'img/class'+classificacao_sistema_aq+'.svg'
    document.getElementById('reducao_aq').innerHTML = 'Redução em relação à condição de referência: - %'
  }
  else{
    document.getElementById('consumo_aq').innerHTML = aq_real + ' kWh/ano' 
    document.getElementById('img_aq').src = 'img/class'+classificacao_sistema_aq+'.svg'
    document.getElementById('reducao_aq').innerHTML = 'Redução em relação à condição de referência: '+ Math.round(dif_sistema_aq)+' %'
  }


  //classificação geral
  if (classificacao_ed == 'erro'){
    window.alert('Existe algum erro na compilação do consumo total da edificação, verifique o preenchimento de todos os sistemas')
    document.getElementById('consumo_geral').innerHTML = '- kWh/ano' 
    document.getElementById('img_geral').src = 'img/classZero.svg'
    document.getElementById('reducao_geral').innerHTML = 'Redução em relação à condição de referência: - %'
    document.getElementById('classe_sem_geracao').innerHTML = '-'
    document.getElementById('reducao_total').innerHTML = '-'
  }

  if (classificacao_ed_sem == 'Zero'){
    document.getElementById('classe_sem_geracao').innerHTML = '-'
  }

  else{
    document.getElementById('consumo_geral').innerHTML = Math.round(CEPreal) + ' kWh/ano' 
    document.getElementById('img_geral').src = 'img/class'+classificacao_ed+'.svg'
    document.getElementById('reducao_geral').innerHTML = 'Redução em relação à condição de referência: '+ Math.round(REDCEP_geral)+' %'
    document.getElementById('reducao_total').innerHTML = Math.round(REDCEP_geral)
    document.getElementById('classe_sem_geracao').innerHTML = classificacao_ed_sem
    document.getElementById('consumo_total_ref').innerHTML = Number((CEPref)/(parseFloat(document.getElementById('area_construida').value))).toFixed(2)
    document.getElementById('consumo_total_real').innerHTML =Number((CEPreal)/(parseFloat(document.getElementById('area_construida').value))).toFixed(2)
    document.getElementById('reducao_total').innerHTML = Number(REDCEP_geral).toFixed(2);
    document.getElementById('emissoes_real').innerHTML = Number(emissao_real/1000).toFixed(2); // divide por 1000 para indicar tonelada
    document.getElementById('emissoes_ref').innerHTML = Number(emissao_ref/1000).toFixed(2); // divide por 1000 para indicar tonelada
    document.getElementById('reducao_emissoes').innerHTML = Number(red_emissoes).toFixed(2); // divide por 1000 para indicar tonelada
  }
  
  document.getElementById('area_resumo').innerHTML = document.getElementById('area_construida').value;
  document.getElementById('nome_ed').innerHTML = document.getElementById('nome_projeto').value;

}

